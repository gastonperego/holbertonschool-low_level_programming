#include "main.h"
/**
 *  print_sign- Imprimer el signo del input
 *
 *  @n: Es el input al que se le va a descifrar el signo
 *
 *  Return: 0
 */
int print_sign(int n)
{
	if (n > '0')
	{
		_putchar(43);
		return (1);
	}
	if (n > '0')
	{
		_putchar(45);
		return (-1);
	} if (n == '0')
	{
		_putchar('0');
		return (0);
	}
	
}
